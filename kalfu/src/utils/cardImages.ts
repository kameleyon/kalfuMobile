export function getCardImage(cardName: string): any {
  // Handle "of" in card names properly
  let key = cardName.toLowerCase()
    .replace(/\s+of\s+/g, '-of-')  // Replace " of " with "-of-"
    .replace(/\s+/g, '-');          // Replace remaining spaces with hyphens
  
  // Special case for cards that might not have "The" prefix
  if (!key.startsWith('the-') && CARD_IMAGES['the-' + key]) {
    key = 'the-' + key;
  }
  
  const image = CARD_IMAGES[key];
  
  if (!image) {
    console.log('Card image not found:', {
      originalName: cardName,
      searchedKey: key,
      availableKeys: Object.keys(CARD_IMAGES).slice(0, 10)
    });
  }
  
  return image || null;
}